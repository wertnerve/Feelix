# busylight_commands.py

"""
Command templates for the Busylight device.
Each command is a 65-byte array that controls the light's behavior.

Command structure:
Byte 0: Command type (0x00 for light control)
Byte 1: Step control (0x10 for single step)
Byte 2: Repeat count (0x01 for single execution)
Byte 3: Red intensity (0x00-0x64)
Byte 4: Green intensity (0x00-0x64)
Byte 5: Blue intensity (0x00-0x64)
Byte 6: ON time (0x01 = 0.1s)
Byte 7: OFF time (0x00 = no off time)
Byte 8: Ringtone control (0x80 = no change)
"""

